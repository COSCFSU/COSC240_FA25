# program that will translate texting words to human words

word = input("What word do you need translated? >")

if (word == "lol"):
    print("That means laughing out loud")
elif (word == "omw"):
    print("That's on my way")
elif (word == "o7"):
    print("That's a salute")
elif (word == "orz"):
    print("That's a dude with his head on the ground like in an anime apology")
elif (word == "brb"):
    print("That means be right back")
elif (word == "w"):
    print("That means lol, in Japanese")
else:
    print("Sorry, I don't know that one")


